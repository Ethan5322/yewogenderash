/**
 * Generates every rendering of the brand mark from one geometric definition.
 *
 * The mark is a heart cradled in open hands. It is built here from primitives
 * (palm, fingers, thumbs) rather than hand-written path data so the proportions
 * stay editable, then baked into absolute 24x24 path strings and written to:
 *
 *   lib/brand.ts                     - consumed by the site, OG image and ID card
 *   public/brand/logo-mark.svg       - light surfaces
 *   public/brand/logo-mark-ondark.svg- dark surfaces (ID card)
 *   public/brand/*.png               - raster copies for social/schema.org
 *   app/icon.svg                     - favicon (reversed out of a filled tile)
 *
 * Run: node scripts/make-brand-assets.mjs
 */
import { writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const GREEN = "#0f7a4d";
const GOLD = "#c8922a";
const GOLD_ON_GREEN = "#e8b04b"; // lifted so it holds contrast against the tile

const n = (v) => Number(v.toFixed(2));
const rot = (x, y, cx, cy, t) => {
  const c = Math.cos(t), s = Math.sin(t);
  return [cx + (x - cx) * c - (y - cy) * s, cy + (x - cx) * s + (y - cy) * c];
};

/** A finger/capsule standing on `base`, rotated outward about its own base. */
function finger(cx, tip, deg, r = 0.95, base = 18.4) {
  const t = (deg * Math.PI) / 180;
  const p = (x, y) => rot(x, y, cx, base, t);
  const [a, b] = p(cx - r, base), [c, d] = p(cx - r, tip + r);
  const [e, g] = p(cx + r, tip + r), [h, i] = p(cx + r, base);
  return `M${n(a)} ${n(b)} L${n(c)} ${n(d)} A${r} ${r} 0 0 1 ${n(e)} ${n(g)} L${n(h)} ${n(i)} Z`;
}

/** A thumb: a capsule from `a` (inside the palm) to `b`, with a rounded tip. */
function thumb(ax, ay, bx, by, r) {
  const dx = bx - ax, dy = by - ay, L = Math.hypot(dx, dy);
  const px = (-dy / L) * r, py = (dx / L) * r;
  return `M${n(ax + px)} ${n(ay + py)} L${n(bx + px)} ${n(by + py)} ` +
         `A${r} ${r} 0 0 0 ${n(bx - px)} ${n(by - py)} L${n(ax - px)} ${n(ay - py)} Z`;
}

/** Palm: a wide cup. Written with explicit L/A so it survives transforming. */
const palm =
  "M10 16.6 L14 16.6 A2.9 2.9 0 0 1 16.9 19.5 A2.9 2.9 0 0 1 14 22.1 " +
  "L10 22.1 A2.9 2.9 0 0 1 7.1 19.5 A2.9 2.9 0 0 1 10 16.6 Z";

const HAND_RAW = [
  palm,
  finger(8.6, 12.6, -32),
  finger(15.4, 12.6, 32),
  thumb(8.8, 19.9, 5.0, 17.6, 1.1),
  thumb(15.2, 19.9, 19.0, 17.6, 1.1),
];

// Heart, then settled down into the cup of the hands.
const HEART_RAW =
  "M12 13 C12 13 7.5 10.2 7.5 7.1 C7.5 5.5 8.75 4.2 10.35 4.2 " +
  "C11.15 4.2 11.7 4.7 12 5.2 C12.3 4.7 12.85 4.2 13.65 4.2 " +
  "C15.25 4.2 16.5 5.5 16.5 7.1 C16.5 10.2 12 13 12 13 Z";

/** Uniform scale + translate applied to absolute M/L/C/A/Z path data. */
function transform(d, s, tx, ty) {
  const X = (v) => n(v * s + tx);
  const Y = (v) => n(v * s + ty);
  return d.replace(/([MLCAZ])([^MLCAZ]*)/gi, (_, cmd, rest) => {
    const a = rest.trim().split(/[\s,]+/).filter(Boolean).map(Number);
    if (cmd === "Z") return "Z";
    if (cmd === "M" || cmd === "L") return `${cmd}${X(a[0])} ${Y(a[1])} `;
    if (cmd === "C") return `C${X(a[0])} ${Y(a[1])} ${X(a[2])} ${Y(a[3])} ${X(a[4])} ${Y(a[5])} `;
    // A rx ry rot large-arc sweep x y — uniform scale, so radii just scale.
    return `A${n(a[0] * s)} ${n(a[1] * s)} ${a[2]} ${a[3]} ${a[4]} ${X(a[5])} ${Y(a[6])} `;
  }).replace(/\s+/g, " ").trim();
}

// The raw mark sits low and small in the box; scale it up about its own centre
// and recentre so it fills the 24x24 grid evenly.
const S = 1.24, CX = 12, CY = 15.2, TARGET_Y = 12.2;
const tx = CX - S * CX;
const ty = TARGET_Y - S * CY;

const HAND = HAND_RAW.map((d) => transform(d, S, tx, ty));
// Heart is first scaled to 0.95 in place, then carried through the same fit.
const HEART = transform(transform(HEART_RAW, 0.95, 12 - 0.95 * 12, 12.4 - 0.95 * 8.6), S, tx, ty);

/* ---------------------------------- emit --------------------------------- */

writeFileSync(join(ROOT, "lib/brand.ts"),
`/**
 * Brand mark — single source of truth. GENERATED by scripts/make-brand-assets.mjs.
 * Do not hand-edit: change the geometry in that script and re-run it.
 *
 * The mark is a heart cradled in open hands — the hands are the platform holding
 * a giver's contribution, the heart is the cause. Every path is drawn on a 24x24
 * grid so the same strings feed SVG (site, favicon, OG image) and Path2D (the
 * canvas-rendered fundraiser ID).
 */

/** Open hands: palm, two splayed fingers, two thumbs. Filled as separate
 * subpaths — merging them into one string makes overlaps cancel under the
 * nonzero fill rule and punches holes in the mark. */
export const BRAND_HAND_PATHS: readonly string[] = [
${HAND.map((d) => `  "${d}",`).join("\n")}
];

/** Heart, resting in the cup of the hands. Draw it after the hands. */
export const BRAND_HEART_PATH = "${HEART}";

export const BRAND_GREEN = "${GREEN}";
export const BRAND_GOLD = "${GOLD}";
`, "utf8");

const marks = (handFill, heartFill) =>
  `<path d="${HEART}" fill="${heartFill}"/>` +
  HAND.map((d) => `<path d="${d}" fill="${handFill}"/>`).join("");

const svgFile = (comment, handFill, heartFill) =>
`<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" width="512" height="512" role="img" aria-label="Yewogen Derash">
  <!-- ${comment} -->
  ${marks(handFill, heartFill)}
</svg>
`;

writeFileSync(join(ROOT, "public/brand/logo-mark.svg"),
  svgFile("Giving mark: a heart cradled in open hands. Transparent background.", GREEN, GOLD), "utf8");
writeFileSync(join(ROOT, "public/brand/logo-mark-ondark.svg"),
  svgFile("On-dark variant for the ID card and dark surfaces.", "#ffffff", GOLD_ON_GREEN), "utf8");

writeFileSync(join(ROOT, "app/icon.svg"),
`<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" width="32" height="32">
  <!-- Favicon: reversed out of a filled tile so the mark holds up at 16px. -->
  <rect width="24" height="24" rx="5.5" fill="${GREEN}"/>
  <g transform="translate(12 12) scale(0.82) translate(-12 -12)">${marks("#ffffff", GOLD_ON_GREEN)}</g>
</svg>
`, "utf8");

for (const [name, file] of [["logo-mark", "logo-mark"], ["logo-mark-ondark", "logo-mark-ondark"]]) {
  const svg = svgFile("", name === "logo-mark" ? GREEN : "#ffffff",
                      name === "logo-mark" ? GOLD : GOLD_ON_GREEN);
  await sharp(Buffer.from(svg), { density: 600 })
    .resize(1024, 1024, { fit: "contain", background: { r: 0, g: 0, b: 0, alpha: 0 } })
    .png()
    .toFile(join(ROOT, `public/brand/${file}.png`));
}

console.log("Brand assets regenerated.");
